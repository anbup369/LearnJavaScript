// Assignment Operators (=, +=, -=, *=, /=, %=)
// ------------------------------------------------
// These operators assign a value to the left operand based on the right operand.

let x = 10;

// x = x + 10; -> The short way to write this is:
x += 10;
console.log(x); // 20

x -= 3; // x = x - 3;
console.log(x); // 17

x *= 2; // x = x * 2;
console.log(x); // 34

x /= 4; // x = x / 4;
console.log(x); // 8.5

x %= 4; // x = x % 4; (Remainder of 8.5 divided by 4)
console.log(x); // 0.5